package collections;

public class InputClass {
	String animalName;
	int lifeSpan;
	float existencePercentage;
	String country;
	public String getAnimalName() {
		return animalName;
	}
	public void setAnimalName(String animalName) {
		this.animalName = animalName;
	}
	public int getLifeSpan() {
		return lifeSpan;
	}
	public void setLifeSpan(int lifeSpan) {
		this.lifeSpan = lifeSpan;
	}
	public float getExistencePercentage() {
		return existencePercentage;
	}
	public void setExistencePercentage(float existencePercentage) {
		this.existencePercentage = existencePercentage;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	
	
}
