package Operators;

public class RelationalOperators {
	
	boolean isEqualTo(int input1, int input2) {
		return input1==input2;
	}
	
	boolean notEqualTo(int input1, int input2) {
		return input1!=input2;
	}
	
	boolean lessThan(int input1, int input2) {
		return input1<input2;
	}
	
	boolean greaterThan(int input1, int input2) {
		return input1>input2;
	}
	
	boolean lessThanEquals(int input1, int input2) {
		return input1<=input2;
	}
	
	boolean greaterThanEquals(int input1, int input2) {
		return input1>=input2;
	}

}
